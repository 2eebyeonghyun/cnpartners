<script>
    function frmCheck() {

        var f = document.contactFrm;

        if (!f.agr_btn.checked) {
            alert('개인정보 수집·이용 동의 여부에 동의하셔야 합니다.');
            return false;
        }

        if ($("#frm_subject").val() == '') {
            alert("제목을 입력해 주세요.");
            $("#frm_subject").focus();
            return false;
        }
        if ($("#frm_name").val() == '') {
            alert("이름을 입력해 주세요.");
            $("#frm_name").focus();
            return false;
        }
        if ($("#frm_tel").val() == '') {
            alert("휴대폰 번호를 입력해 주세요.");
            $("#frm_tel").focus();
            return false;
        }
        if ($.trim($("#frm_tel").val()).length < 10 || $.trim($("#frm_tel").val()).length > 13) {
            alert("연락처는 10~13자리 입니다.");
            $("#frm_tel").select();
            return false;
        }
        if ($("#frm_email").val() == '') {
            alert("이메일을 입력해 주세요.");
            $("#frm_email").focus();
            return false;
        }
        if ($("#frm_contents").val() == '') {
            alert("문의내용을 입력해 주세요.");
            $("#frm_contents").focus();
            return false;
        }

        f.submit();
    }
</script>
